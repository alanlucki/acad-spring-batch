package com.example.acadspringbatch.listener;

import javax.batch.runtime.BatchStatus;
import javax.batch.runtime.JobExecution;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.batch.core.listener.JobExecutionListenerSupport;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.jdbc.core.JdbcTemplate;
import org.springframework.stereotype.Component;

import com.example.acadspringbatch.model.Comercio;

@Component
public class JobListener extends JobExecutionListenerSupport {

    private static final Logger LOG = LoggerFactory.getLogger(JobListener.class);

    private JdbcTemplate jdbcTemplate;

    @Autowired
    public JobListener(JdbcTemplate jdbcTemplate) {
        super();
        this.jdbcTemplate = jdbcTemplate;
    }

    public void afterJob(JobExecution jobExecution) {

        if (jobExecution.getBatchStatus() == BatchStatus.COMPLETED) {
            LOG.info("Finalizo el Job, verifica los resultados");
            
            jdbcTemplate
                    .query(
                            "SELECT contrato , comercio , validacion FROM comercio",
                            (rs, row) -> new Comercio(rs.getString(1), rs.getString(2), rs.getString(3)))
                    .forEach(
                            persona -> LOG.info("Resgistro<" + persona + ">"));

        }
        if (jobExecution.getBatchStatus() == BatchStatus.STARTING) {
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("Iniciando Job");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            
        }
        if (jobExecution.getBatchStatus() == BatchStatus.FAILED) {
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            
            LOG.info("Job ha fallado");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            LOG.info("###################################################################################");
            
        }


    }
}
